package session_4;

public class Date {
	private int dd,mm,yy;
	
	public Date()
	{}

	public Date(int dd, int mm, int yy) {
		super();
		this.dd = dd;
		this.mm = mm;
		this.yy = yy;
	}

	@Override
	public String toString() {
		return "Date1 [dd=" + dd + ", mm=" + mm + ", yy=" + yy + "]";
	}
	
	

}
